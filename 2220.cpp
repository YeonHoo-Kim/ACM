#include<cstdio>

int a[100001];

int main() {

	int n;
	scanf("%d", &n);
	for (int i = 1; i < n; i++) {
		for (int j = i; j > 1; j /= 2)
			a[j] = a[j / 2];
		a[1] = i + 1;
	}
	a[n] = 1;

	for (int i = 1; i <= n; i++)
		printf("%d ", a[i]);
	printf("\n");

	return 0;
}
